// Contar de 0 a 10
let inicioDaContagem = 1
let contador = inicioDaContagem

// While = enquanto
// O computador repetira o codigo dentro de {} enquanto a condição for verdadeira. No nossa caso, o contador vai até o limite 100, se passar disso ele para, porque 101 é maior
// que 100, portando contador <= 100 é falso.
while (contador <= 100) {
    console.log(contador);
    contador = contador + 2
}